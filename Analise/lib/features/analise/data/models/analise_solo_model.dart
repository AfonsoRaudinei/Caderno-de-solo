import 'package:soloforte/features/analise/domain/entities/analise_solo.dart';

class AnaliseSoloModel extends AnaliseSolo {
  const AnaliseSoloModel({
    required super.id,
    required super.fazenda,
    required super.produtor,
    required super.talhao,
    required super.numeroAmostra,
    required super.cultura,
    required super.safra,
    required super.laboratorio,
    required super.dataCadastro,
    required super.profundidade,
    super.latitude,
    super.longitude,
    super.descricaoLocal,
    super.argila,
    super.silte,
    super.areiaTotal,
    super.phAgua,
    super.phSmp,
    super.phCaCl2,
    super.materiaOrganica,
    super.carbonoOrganico,
    super.pMehlich,
    super.pResina,
    super.pRem,
    super.s020,
    super.s2040,
    super.k,
    super.ca,
    super.mg,
    super.al,
    super.hMaisAl,
    super.na,
    super.b,
    super.cu,
    super.fe,
    super.mn,
    super.zn,
    super.ni,
    super.mo,
    super.se,
    super.pdfUrl,
    super.laudoMetadata,
    this.userId = '',
  });

  final String userId;

  factory AnaliseSoloModel.fromJson(Map<String, dynamic> json) {
    double? asDouble(dynamic value) {
      if (value == null) return null;
      if (value is num) return value.toDouble();
      if (value is String) return double.tryParse(value.replaceAll(',', '.'));
      return null;
    }

    return AnaliseSoloModel(
      id: json['id'] as String,
      userId: (json['userId'] ?? '') as String,
      fazenda: (json['fazenda'] ?? json['propriedade'] ?? '') as String,
      produtor: (json['produtor'] ?? json['produtorId'] ?? '') as String,
      talhao: (json['talhao'] ?? json['nomeArea'] ?? '') as String,
      numeroAmostra: (json['numeroAmostra'] ?? '') as String,
      cultura: Cultura.values.firstWhere(
        (e) => e.name == json['cultura'],
        orElse: () => Cultura.soja,
      ),
      safra: json['safra'] as String,
      laboratorio: json['laboratorio'] as String,
      dataCadastro: DateTime.parse(json['dataCadastro'] as String),
      profundidade: (json['profundidade'] ?? '') as String,
      latitude: asDouble(json['latitude']),
      longitude: asDouble(json['longitude']),
      descricaoLocal: json['descricaoLocal'] as String?,
      argila: asDouble(json['argila']),
      silte: asDouble(json['silte']),
      areiaTotal: asDouble(json['areiaTotal']),
      phAgua: asDouble(json['phAgua']),
      phSmp: asDouble(json['phSmp']),
      phCaCl2: asDouble(json['phCaCl2'] ?? json['phCacl2']),
      materiaOrganica: asDouble(json['materiaOrganica']),
      carbonoOrganico: asDouble(json['carbonoOrganico']),
      pMehlich: asDouble(json['pMehlich'] ?? json['fosforo']),
      pResina: asDouble(json['pResina']),
      pRem: asDouble(json['pRem']),
      s020: asDouble(json['s020'] ?? json['enxofre']),
      s2040: asDouble(json['s2040']),
      k: asDouble(json['k'] ?? json['potassio']),
      ca: asDouble(json['ca'] ?? json['calcio']),
      mg: asDouble(json['mg'] ?? json['magnesio']),
      al: asDouble(json['al'] ?? json['aluminio']),
      hMaisAl: asDouble(json['hMaisAl']),
      na: asDouble(json['na']),
      b: asDouble(json['b'] ?? json['boro']),
      cu: asDouble(json['cu'] ?? json['cobre']),
      fe: asDouble(json['fe'] ?? json['ferro']),
      mn: asDouble(json['mn'] ?? json['manganes']),
      zn: asDouble(json['zn'] ?? json['zinco']),
      ni: asDouble(json['ni']),
      mo: asDouble(json['mo']),
      se: asDouble(json['se']),
      pdfUrl: json['pdfUrl'] as String?,
      laudoMetadata: json['laudoMetadata'] as Map<String, dynamic>?,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      // Chaves canônicas
      'id': id,
      'userId': userId,
      'fazenda': fazenda,
      'produtor': produtor,
      'talhao': talhao,
      'numeroAmostra': numeroAmostra,
      'cultura': cultura.name,
