import 'package:soloforte/features/analise/domain/entities/analise_solo.dart';

class AnaliseSoloModel extends AnaliseSolo {
  const AnaliseSoloModel({
    required super.id,
    required super.produtorId,
    required super.nomeArea,
    required super.cultura,
    required super.safra,
    required super.laboratorio,
    required super.dataCadastro,
    super.latitude,
    super.longitude,
    super.descricaoLocal,
    required super.textura,
    required super.profundidade,
    super.phAgua,
    super.phSmp,
    super.phCacl2,
    super.fosforo,
    super.potassio,
    super.calcio,
    super.magnesio,
    super.enxofre,
    super.aluminio,
    super.hMaisAl,
    super.boro,
    super.cobre,
    super.ferro,
    super.manganes,
    super.zinco,
    super.pdfUrl,
  });

  factory AnaliseSoloModel.fromJson(Map<String, dynamic> json) {
    return AnaliseSoloModel(
      id: json['id'] as String,
      produtorId: json['produtorId'] as String,
      nomeArea: json['nomeArea'] as String,
      cultura: Cultura.values.firstWhere(
        (e) => e.name == json['cultura'],
        orElse: () => Cultura.soja,
      ),
      safra: json['safra'] as String,
      laboratorio: json['laboratorio'] as String,
      dataCadastro: DateTime.parse(json['dataCadastro'] as String),
      latitude: json['latitude'] as double?,
      longitude: json['longitude'] as double?,
      descricaoLocal: json['descricaoLocal'] as String?,
      textura: TexturaSolo.values.firstWhere(
        (e) => e.name == json['textura'],
        orElse: () => TexturaSolo.medio,
      ),
      profundidade: json['profundidade'] as String,
      phAgua: json['phAgua'] as double?,
      phSmp: json['phSmp'] as double?,
      phCacl2: json['phCacl2'] as double?,
      fosforo: json['fosforo'] as double?,
      potassio: json['potassio'] as double?,
      calcio: json['calcio'] as double?,
      magnesio: json['magnesio'] as double?,
      enxofre: json['enxofre'] as double?,
      aluminio: json['aluminio'] as double?,
      hMaisAl: json['hMaisAl'] as double?,
      boro: json['boro'] as double?,
      cobre: json['cobre'] as double?,
      ferro: json['ferro'] as double?,
      manganes: json['manganes'] as double?,
      zinco: json['zinco'] as double?,
      pdfUrl: json['pdfUrl'] as String?,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'produtorId': produtorId,
      'nomeArea': nomeArea,
      'cultura': cultura.name,
      'safra': safra,
      'laboratorio': laboratorio,
      'dataCadastro': dataCadastro.toIso8601String(),
      'latitude': latitude,
      'longitude': longitude,
      'descricaoLocal': descricaoLocal,
      'textura': textura.name,
      'profundidade': profundidade,
      'phAgua': phAgua,
      'phSmp': phSmp,
      'phCacl2': phCacl2,
      'fosforo': fosforo,
      'potassio': potassio,
      'calcio': calcio,
      'magnesio': magnesio,
      'enxofre': enxofre,
      'aluminio': aluminio,
      'hMaisAl': hMaisAl,
      'boro': boro,
      'cobre': cobre,
      'ferro': ferro,
      'manganes': manganes,
      'zinco': zinco,
      'pdfUrl': pdfUrl,
    };
  }

  factory AnaliseSoloModel.fromEntity(AnaliseSolo entity) {
    return AnaliseSoloModel(
      id: entity.id,
      produtorId: entity.produtorId,
      nomeArea: entity.nomeArea,
      cultura: entity.cultura,
      safra: entity.safra,
      laboratorio: entity.laboratorio,
      dataCadastro: entity.dataCadastro,
      latitude: entity.latitude,
      longitude: entity.longitude,
      descricaoLocal: entity.descricaoLocal,
      textura: entity.textura,
      profundidade: entity.profundidade,
      phAgua: entity.phAgua,
      phSmp: entity.phSmp,
      phCacl2: entity.phCacl2,
      fosforo: entity.fosforo,
      potassio: entity.potassio,
      calcio: entity.calcio,
      magnesio: entity.magnesio,
      enxofre: entity.enxofre,
      aluminio: entity.aluminio,
      hMaisAl: entity.hMaisAl,
      boro: entity.boro,
      cobre: entity.cobre,
      ferro: entity.ferro,
      manganes: entity.manganes,
      zinco: entity.zinco,
      pdfUrl: entity.pdfUrl,
    );
  }
}
