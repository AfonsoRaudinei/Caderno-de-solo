      'profundidade': profundidade,
      'latitude': latitude,
      'longitude': longitude,
      'descricaoLocal': descricaoLocal,
      'argila': argila,
      'silte': silte,
      'areiaTotal': areiaTotal,
      'phAgua': phAgua,
      'phSmp': phSmp,
      'phCaCl2': phCaCl2,
      'materiaOrganica': materiaOrganica,
      'carbonoOrganico': carbonoOrganico,
      'pMehlich': pMehlich,
      'pResina': pResina,
      'pRem': pRem,
      's020': s020,
      's2040': s2040,
      'k': k,
      'ca': ca,
      'mg': mg,
      'al': al,
      'hMaisAl': hMaisAl,
      'na': na,
      'b': b,
      'cu': cu,
      'fe': fe,
      'mn': mn,
      'zn': zn,
      'ni': ni,
      'mo': mo,
      'se': se,
      'pdfUrl': pdfUrl,
      'laudoMetadata': laudoMetadata,

      // Chaves legadas para compatibilidade
      'produtorId': produtor,
      'nomeArea': talhao,
      'phCacl2': phCaCl2,
      'fosforo': pMehlich,
      'potassio': k,
      'calcio': ca,
      'magnesio': mg,
      'enxofre': s020,
      'aluminio': al,
      'boro': b,
