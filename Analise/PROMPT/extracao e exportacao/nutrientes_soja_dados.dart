// ─── MODELO DE DADOS ──────────────────────────────────────────
class NutrientRecord {
  final double N, P, K, Ca, Mg, S, B, Cu, Fe, Mn, Zn, Mo, Co, Ni, Se;
  const NutrientRecord({
    required this.N,
    required this.P,
    required this.K,
    required this.Ca,
    required this.Mg,
    required this.S,
    required this.B,
    required this.Cu,
    required this.Fe,
    required this.Mn,
    required this.Zn,
    required this.Mo,
    required this.Co,
    required this.Ni,
    required this.Se,
  });

  double operator [](String key) => switch (key) {
        'N' => N,
        'P' => P,
        'K' => K,
        'Ca' => Ca,
        'Mg' => Mg,
        'S' => S,
        'B' => B,
        'Cu' => Cu,
        'Fe' => Fe,
        'Mn' => Mn,
        'Zn' => Zn,
        'Mo' => Mo,
        'Co' => Co,
        'Ni' => Ni,
        'Se' => Se,
        _ => 0.0,
      };
}

class SourceData {
  final NutrientRecord exportacao;
  final NutrientRecord extracao;
  const SourceData({required this.exportacao, required this.extracao});
}

