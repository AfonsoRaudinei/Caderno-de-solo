// ─── HELPERS ──────────────────────────────────────────────────

bool isMacro(String nutrient) => kMacronutrientes.contains(nutrient);

class AbsorcaoResult {
  final String stage;
  final double percentage;
  final double value;
  final String unit;
  const AbsorcaoResult({
    required this.stage,
    required this.percentage,
    required this.value,
    required this.unit,
  });
}

/// Calcula absorção por estádio fenológico
/// [mode]: 'accumulated' ou 'perStage'
List<AbsorcaoResult> calcAbsorcao({
  required String sourceType,
  required String source,
  required String nutrient,
  required String dataType, // 'Exportação' ou 'Extração'
  required double yieldTons,
  String mode = 'accumulated',
}) {
  final baseValue = kNutrientData[sourceType]?[source]?.let((s) =>
          dataType == 'Exportação'
              ? s.exportacao[nutrient]
              : s.extracao[nutrient]) ??
      0.0;

  final percentages = kAbsorcaoPercentuais[nutrient]!;
  final macro = isMacro(nutrient);
  final valuePerTon = macro ? baseValue : baseValue / 1000.0;
  final totalKg = valuePerTon * yieldTons;
  final unit = totalKg < 1.0 ? 'g' : 'kg';
  final multiplier = unit == 'g' ? 1000.0 : 1.0;

  return List.generate(kStadiosKeys.length, (index) {
    final key = kStadiosKeys[index];
    final cumPct = percentages[key]!;
    final prevPct = index > 0 ? percentages[kStadiosKeys[index - 1]]! : 0.0;
    final pct = mode == 'accumulated' ? cumPct : cumPct - prevPct;
    final value = (pct / 100.0) * totalKg * multiplier;
    return AbsorcaoResult(
      stage: kStadios[index],
      percentage: pct,
      value: double.parse(value.toStringAsFixed(4)),
      unit: unit,
    );
  });
}

